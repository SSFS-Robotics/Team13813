public class Ball {
    private Integer x;
    private Integer y;
    private Integer r;

    public Ball(Integer x, Integer y, Integer r) {
        x = this.x;
        y = this.y;
        r = this.r;
    }

    public Integer getX() {
        return this.x;
    }
    public Integer getY() {
        return this.y;
    }
    public Integer getR() {
        return this.r;
    }
}
